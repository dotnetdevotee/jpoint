package ru.jug;

public class User {

	private String address;
	private String name;

	public User() {
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}
	
	public String getName() {
		return name;
	}

	public void setName(String address) {
		this.name = address;
	}
	
}